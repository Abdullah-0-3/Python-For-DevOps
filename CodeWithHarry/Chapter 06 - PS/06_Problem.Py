marks = int(input("Enter your marks: "))

if marks >= 90:
    print("You secured A+ grade")
elif marks >= 80:
    print("You secured A grade")
elif marks >= 70:
    print("You secured B grade")
elif marks >= 60:
    print("You secured C grade")
elif marks >= 50:
    print("You secured D grade")
else:
    print("You secured F grade")