# Intersection and Union of Sets

s1 = {1, 2, 3, 4, 5}
s2 = {4, 5, 6, 7, 8}
s3 = {1, 2, 3}

# Intersection of s1 and s2
print('Intersection of s1 and s2:', s1.intersection(s2))

# Intersection of s1 and s3
print('Intersection of s1 and s3:', s1.intersection(s3))

# Union of s1 and s2
print('Union of s1 and s2:', s1.union(s2))

# Union of s1 and s3
print('Union of s1 and s3:', s1.union(s3))

'''
Union: The union of two sets is the set of elements that are in either set.
Intersection: The intersection of two sets is the set of elements that are in both sets.
'''